    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Visit::class);
    }

    /**
     * @param string $sortBy
     * @param string $sortDir
     * @param string|null $device
     * @return []
     */
    public function ranking(string $sortBy, string $sortDir, string $device = null): array
    {
        if ($sortBy == 'total_visits') {
            $sortBy = 'COUNT(u.id)';
        }

        return $this->createQueryBuilder('v')
            ->select('u, COUNT(u.id) as total_visits')
            ->join('App\Entity\UrlShortened', 'u')
            ->groupBy('u.id')
            ->orderBy($sortBy, $sortDir)
            ->getQuery()
            ->getResult();
    }

